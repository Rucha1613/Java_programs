public class OperatorSample
{
  public static void main(String args[])
   {
     int a = 60;
     int b = 20;
     int c = 25;
     int d = 25;
 
     a += 2;
     b -= 2;
     c *= 2;
     d /= 2; 
     System.out.println(" a += 2 :" + a); 
     System.out.println("  b -= 2 :" + b); 
     System.out.println("  c *= 2 :" + c); 
     System.out.println("d /= 2 :" + d);  
  }
}